	(
	CITY VARCHAR(50),
	UTILITIES_MONTHYLY TEXT,
	AMOUNT REAL
	);

	SELECT * FROM UTILIES_COST_SET;

	DROP TABLE IF EXISTS SPPORTS_LEISURE_COST_SET;

	CREATE TABLE SPPORTS_LEISURE_COST_SET
	(
	CITY VARCHAR(50),
	SPORTS_AND_LEISURE TEXT,
	AMOUNT REAL
	);

	SELECT * FROM SPPORTS_LEISURE_COST_SET;

	DROP TABLE IF EXISTS CHILDCARE;

	CREATE TABLE CHILDCARE
	(
	CITY VARCHAR(50),
	CHILDCARE TEXT,
	AMOUNT REAL
	);

	SELECT * FROM CHILDCARE;

