	AMOUNT REAL
	);

	SELECT * FROM RENT_COST_SET;

	DROP TABLE IF EXISTS BUY_APT_COST_SET;

	CREATE TABLE BUY_APT_COST_SET
	(
	CITY VARCHAR(50),
	BUY_APARTMENT_PRICE TEXT,
	AMOUNT REAL
	);

	SELECT * FROM BUY_APT_COST_SET;

	DROP TABLE IF EXISTS SALARY_AND_FINANCING_COST;

	CREATE TABLE SALARY_AND_FINANCING_COST
	(
	CITY VARCHAR(50),
